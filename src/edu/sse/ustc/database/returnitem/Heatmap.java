public class Heatmap {
	private int point_x;
	private int point_y;
	private int point_z;
	private int loc_times;
	public int getPoint_x() {
		return point_x;
	}
	public void setPoint_x(int point_x) {
		this.point_x = point_x;
	}
	public int getPoint_y() {
		return point_y;
	}
	public void setPoint_y(int point_y) {
		this.point_y = point_y;
	}
	public int getPoint_z() {
		return point_z;
	}
	public void setPoint_z(int point_z) {
		this.point_z = point_z;
	}
	public int getLoc_times() {
		return loc_times;
	}
	public void setLoc_times(int loc_times) {
		this.loc_times = loc_times;
	}


	

}
