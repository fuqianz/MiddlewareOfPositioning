package edu.sse.ustc.database.returnitem;

public class VisitedRecords {
	private String visited_date;
	private String firstenter_time;
	private String lastleave_time;
	public String getVisited_date() {
		return visited_date;
	}
	public void setVisited_date(String visited_date) {
		this.visited_date = visited_date;
	}
	public String getFirstenter_time() {
		return firstenter_time;
	}
	public void setFirstenter_time(String firstenter_time) {
		this.firstenter_time = firstenter_time;
	}
	public String getLastleave_time() {
		return lastleave_time;
	}
	public void setLastleave_time(String lastleave_time) {
		this.lastleave_time = lastleave_time;
	}
	

}
